    public void cargarContenedores() {
        DefaultComboBoxModel mm = new DefaultComboBoxModel();
        for (Persona p : listPersonas) {
            mm.addElement(p);
        }
        cb_modificarPER.setModel(mm);

        CB_amigosU.setModel(mm);
        DefaultComboBoxModel mm1 = new DefaultComboBoxModel();
        for (Objeto o : objetos) {
            mm1.addElement(o);
        }
        cb_modificarOBJ.setModel(mm1);
        cb_objetos.setModel(mm1);

        DefaultTableModel mt = (DefaultTableModel) tablaPersonas.getModel();
        while (mt.getRowCount() > 0) {
            mt.removeRow(0);
        }

        for (Persona p : listPersonas) {
            Object row[] = {p.getNombre(), p.getEdad(), p.getID(), p.getSexo()};
            mt.addRow(row);
        }
        tablaPersonas.setModel(mt);

        DefaultTableModel mt1 = (DefaultTableModel) tablaObjetos.getModel();
        while (mt1.getRowCount() > 0) {
            mt1.removeRow(0);
        }

        for (Objeto o : objetos) {
            String nombre = "";
            if (o instanceof Zapato) {
                nombre = "Zapato";
            } else {
                if (o instanceof Ropa) {
                    nombre = "Ropa";
                } else {
                    nombre = "ObjHogar";
                }
            }
            Object row[] = {nombre, o.getMarca(), o.getColor()};
            mt1.addRow(row);
        }
        tablaObjetos.setModel(mt1);

        tablaObjetos.setEnabled(true);
        tablaPersonas.setEnabled(true);
    }


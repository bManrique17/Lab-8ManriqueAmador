    public String cifradoCesar(String aCodificar, int codigo) {

        String codificado = "";
        aCodificar = aCodificar.toUpperCase();
        int clave = codigo;
        char letra;
        for (int i = 0; i < aCodificar.length(); i++) {
            int codigoChar = aCodificar.charAt(i);
            //65-90
            if (codigoChar + clave > 90) {
                letra = (char) (codigoChar + clave - 90 + 64);
            } else {
                letra = (char) (codigoChar + clave);
            }
            codificado += letra;
        }
        return codificado;
    }

    public String DEScifradoCesar(String aCodificar, int codigo) {

        String codificado = "";
        aCodificar = aCodificar.toUpperCase();
        int clave = codigo * (-1);
        char letra;
        for (int i = 0; i < aCodificar.length(); i++) {
            int codigoChar = aCodificar.charAt(i);
            //65-90
            if (codigoChar - clave < 65) {
                letra = (char) (codigoChar + clave + 90 - 64);
            } else {
                letra = (char) (codigoChar + clave);
            }
            codificado += letra;
        }
        return codificado;
    }

    public String vigenere(String mensaje, String codigo){
        String codificado = "";
        int cont = 0,cod=0;
        for (int i = 0; i < mensaje.length(); i++) {
            if(cont<codigo.length()){
                cod = codigo.charAt(cont)-64;
                cont++;
                codificado+=(char) (mensaje.charAt(i)+cod);
            }else{
                cont=0;
                cod = codigo.charAt(cont)-64;
                codificado+=(char) (mensaje.charAt(i)+cod);
            }
            codificado+=mensaje.charAt(i);
        }
        return codificado;
    }
    
